/**
 * Get districts for a given state
 * @param {string} state
 * @returns {{ name: string, lat: number, lon: number }[]}
 */
export function getDistricts(state) {
  return INDIA_DISTRICTS[state] || [];
}

/**
 * Get a district's lat/lon by state and district name
 */
export function getDistrictCoords(state, districtName) {
  const districts = INDIA_DISTRICTS[state] || [];
  return districts.find(d => d.name === districtName) || null;
}
